@app.route('/api/predict/img/', methods=['POST'])
def predict_full_img():
    item = request.json
    item_dict = json.loads(item, strict=False)
    encodedItem = item_dict['data'].encode('utf8')
    img_data = preprocess(encodedItem)
    jsonResponse = predicts(img_data)
    return jsonResponse


